package com.po4yka.heauton.data.local.database.entities

/**
 * Categories for achievements.
 */
enum class AchievementCategory {
    /**
     * Achievements related to quotes (browsing, favoriting, adding).
     */
    QUOTES,

    /**
     * Achievements related to journaling.
     */
    JOURNALING,

    /**
     * Achievements related to meditation exercises.
     */
    MEDITATION,

    /**
     * Achievements related to breathing exercises.
     */
    BREATHING,

    /**
     * Achievements related to consistency and streaks.
     */
    CONSISTENCY,

    /**
     * Achievements related to sharing and social features.
     */
    SOCIAL,

    /**
     * General achievements (app usage, exploration).
     */
    GENERAL
}
